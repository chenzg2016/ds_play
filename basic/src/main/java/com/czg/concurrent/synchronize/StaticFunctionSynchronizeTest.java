package com.czg.concurrent.synchronize;

/**
 * @author chenzg
 * @date 7/28/21 9:23 AM
 * @description
 */
public class StaticFunctionSynchronizeTest {

    public static synchronized void method1(){
        System.out.println("Method 1 start");
        try {
            System.out.println("Method 1 execute");
            Thread.sleep(3000);
        } catch (InterruptedException e) {
            e.printStackTrace();
        }
        System.out.println("Method 1 end");
    }

    public static synchronized void method2(){
        System.out.println("Method 2 start");
        try {
            System.out.println("Method 2 execute");
            Thread.sleep(1000);
        } catch (InterruptedException e) {
            e.printStackTrace();
        }
        System.out.println("Method 2 end");
    }

    public static void main(String[] args) {
        final StaticFunctionSynchronizeTest test = new StaticFunctionSynchronizeTest();
        final StaticFunctionSynchronizeTest test2 = new StaticFunctionSynchronizeTest();

        new Thread(new Runnable() {
            @Override
            public void run() {
                test.method1();
            }
        }).start();

        new Thread(new Runnable() {
            @Override
            public void run() {
                test2.method2();
            }
        }).start();
    }
}
